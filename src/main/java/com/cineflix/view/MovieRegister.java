    private boolean checkFields()
    {
        if(tfMovieName.getText().trim().isEmpty())
        {
            return false;
        }
        
        if(tfMovieDate.getText().trim().isEmpty())
        {
            return false;
        }
        
        if(tfMovieCategory.getText().trim().isEmpty())
        {
            return false;
        }
        
        if(!tfMovieDate.getText().matches("[0-9]{2}[/][0-9]{2}[/][0-9]{4}"))
        {
            JOptionPane.showMessageDialog(null, "Insira uma data valida!!!: DD/MM/AAAA");
            return false;
        }
        
        return true;
    }
