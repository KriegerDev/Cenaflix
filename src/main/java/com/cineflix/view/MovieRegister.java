    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if(!checkFields())
        {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos com valores validos!!!");
            return;
        }
        
        String _name = tfMovieName.getText();
        String _date = tfMovieDate.getText();
        String _category = tfMovieCategory.getText();
        
        Movie movie = new Movie(0, _name, _date, _category);
        
        MovieController.add(movie);
        
        JOptionPane.showMessageDialog(null, "Filme adicionado!!!");
    }//GEN-LAST:event_btnCadastrarActionPerformed

