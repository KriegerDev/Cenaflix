    private void List()
    {
        List<Movie> movies = MovieController.get();
        
        String[] rows = {"Id", "Nome", "Data lancamento", "Categoria"};
        DefaultTableModel tableModel = new DefaultTableModel(rows, 0);
        
        for(Movie movie: movies) {
            String[] line = {
                String.format("%d", movie.getId()),
                movie.getName(),
                movie.getDate(),
                movie.getCategory()
            };

            tableModel.addRow(line);
        }
        jTable1.setModel(tableModel);
    }
    
    private void List(String _category)
    {
        List<Movie> movies = MovieController.getByCategory(_category);
        
        String[] rows = {"Id", "Nome", "Data lancamento", "Categoria"};
        DefaultTableModel tableModel = new DefaultTableModel(rows, 0);
        
        for(int i=0; i < movies.size(); i++) {
            Movie movie = movies.get(i);

            String[] line = {
                String.format("%d", movie.getId()),
                movie.getName(),
                movie.getDate(),
                movie.getCategory()
            };

            tableModel.addRow(line);
        }
        jTable1.setModel(tableModel);
    }

