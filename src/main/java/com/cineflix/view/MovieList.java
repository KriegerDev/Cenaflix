    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        
        if(UserController.getCurrentUser().getPermission() > 1)
        {
            JOptionPane.showMessageDialog(null, "Você não tem permissão para excluir filmes");
            return;
        }
        
        int row = jTable1.getSelectedRow();
        if(row < 0)
        {
            JOptionPane.showMessageDialog(null, "Selecione um filme para excluir");
            return;
        }
        
        int index = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        if(MovieController.delete(index) == 0)
        {
            JOptionPane.showMessageDialog(null, "Não foi possivel excluir o filme selecionado!!");
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Filme excluido");
        }
        
        this.List();
    }//GEN-LAST:event_btnExcluirActionPerformed

